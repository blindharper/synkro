static Random _rnd;

// Hexadecimal digits.
static const Char* _hex = L"0123456789ABCDEF";

static Byte GetHexDigitValue( Char digit )
{
	for ( Byte b = 0; b < 16; ++b )
	{
		if ( _wcsnicmp(&digit, &_hex[b], 1) == 0 )
			return b;
	}
	return 0;
}

static Float Min( Float a, Float b, Float c )
{
	Float x = a;
	if ( x < b )
		x = b;
	if ( x < c )
		x = c;
	return x;
}

static Float Max( Float a, Float b, Float c )
{
	Float x = a;
	if ( x > b )
		x = b;
	if ( x > c )
		x = c;
	return x;
}

static Color ToColor( const String& color )
{
	if ( color.IsNull() )
	{
		return Color::Transparent;
	}

	if ( color.IsHexadecimal() )
	{
		const UInt len = color.Length();
		if ( len == 6 )
		{
			const Byte r1 = GetHexDigitValue( color[0] );
			const Byte r2 = GetHexDigitValue( color[1] );
			const Byte g1 = GetHexDigitValue( color[2] );
			const Byte g2 = GetHexDigitValue( color[3] );
			const Byte b1 = GetHexDigitValue( color[4] );
			const Byte b2 = GetHexDigitValue( color[5] );
			return Color( ((r1<<4)+r2)/255.0f, ((g1<<4)+g2)/255.0f, ((b1<<4)+b2)/255.0f );
		}
		else if ( len == 3 )
		{
			const Byte r = GetHexDigitValue( color[0] );
			const Byte g = GetHexDigitValue( color[1] );
			const Byte b = GetHexDigitValue( color[2] );
			return Color( ((r<<4)+r)/255.0f, ((g<<4)+g)/255.0f, ((b<<4)+b)/255.0f );
		}
		else if ( len == 2 )
		{
			const Byte c1 = GetHexDigitValue( color[0] );
			const Byte c2 = GetHexDigitValue( color[1] );
			return Color( ((c1<<4)+c2)/255.0f, ((c1<<4)+c2)/255.0f, ((c1<<4)+c2)/255.0f );
		}
		else if ( len == 1 )
		{
			const Byte c = GetHexDigitValue( color[0] );
			return Color( ((c<<4)+c)/255.0f, ((c<<4)+c)/255.0f, ((c<<4)+c)/255.0f );
		}
		else
		{
			return Color::Transparent;
		}
	}
	else
	{
		Double r; Double g; Double b;
		if ( Parser::Parse(color, L"{0,0.000000}, {1,0.000000}, {2,0.000000}", r, g, b) )
			return Color( r, g, b );
	}

	const UInt count = SizeOf( _colorString );
	for ( UInt i = 0; i < count; ++i )
	{
		ColorString c = _colorString[i];
		if ( color.EqualsTo(c.string, true) )
			return c.color;
	}
	return Color::Transparent;
}

Color::Color( Float red, Float green, Float blue ) :
	R( red ), G( green ), B( blue )
{
}

Color::Color( const String& color )
{
	Color col = ToColor( color );
	R = col.R;
	G = col.G;
	B = col.B;
}

Color::Color( Float gray ) :
	R( gray ), G( gray ), B( gray )
{
}

Color::Color() :
	R( 0.0f ), G( 0.0f ), B( 0.0f )
{
}

Bool Color::operator==( const Color& other ) const
{
	return (R == other.R) && (G == other.G) && (B == other.B);
}

Bool Color::operator!=( const Color& other ) const
{
	return (R != other.R) || (G != other.G) || (B != other.B);
}

Color Color::operator*( Float value ) const
{
	return Color( R*value, G*value, B*value );
}

Color Color::operator*( const Color& other ) const
{
	return Color( R*other.R, G*other.G, B*other.B );
}

String Color::ToString() const
{
	constexpr UInt count = SizeOf( _colorString );
	for ( UInt i = 0; i < count; ++i )
	{
		const ColorString c = _colorString[i];
		if ( *this == c.color )
			return c.string;
	}

	if ( *this != Color::Transparent )
	{
		return String::Format( L"{0,0.000000}, {1,0.000000}, {2,0.000000}", this->R, this->G, this->B );
	}

	return String::Empty;
}

Color Color::Inverse() const
{
	return Color( 1.0f-R, 1.0f-G, 1.0f-B );
}

Color Color::Complementary() const
{
	const Float m = Max(R, G, B) + Min(R, G, B);
	return Color( m-R, m-G, m-B );
}

Color Color::GetNext( UInt& index )
{
	index = (index+1 < SizeOf(_colorString)-1) ? index+1 : none;
	return (index != none) ? _colorString[index].color : Color::Transparent;
}

Color Color::Random()
{
	constexpr UInt count = SizeOf( _colorString );
	_rnd.GetInt( count );
	const Int idx = _rnd.GetInt( count );
	return _colorString[idx].color;
}


