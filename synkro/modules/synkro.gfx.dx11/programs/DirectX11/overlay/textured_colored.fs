//------------------------------------------------------------------------------
// File: textured_colored.fs
//
// Purpose: Fragment shader for textured colored overlay
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Structures
//------------------------------------------------------------------------------
struct Fragment
{
	float4 pos	: SV_POSITION;
	float4 col	: COLOR0;
	float2 tex	: TEXCOORD0;
};

//------------------------------------------------------------------------------
// Resources
//------------------------------------------------------------------------------
Texture2D	texTexture;

//------------------------------------------------------------------------------
// Samplers
//------------------------------------------------------------------------------
SamplerState 	samTexture;

//------------------------------------------------------------------------------
// Entry point
//------------------------------------------------------------------------------
float4 main( Fragment fragment ) : SV_TARGET
{
	return texTexture.Sample(samTexture, fragment.tex) * fragment.col;
}
