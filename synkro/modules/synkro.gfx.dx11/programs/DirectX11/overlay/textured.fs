//------------------------------------------------------------------------------
// File: textured.fs
//
// Purpose: Fragment shader for textured overlay
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Structures
//------------------------------------------------------------------------------
struct Fragment
{
	float4 pos	: SV_POSITION;
	float2 tex	: TEXCOORD0;
};

//------------------------------------------------------------------------------
// Parameters
//------------------------------------------------------------------------------
cbuffer cb0 : register( b0 )
{
	float4		p_color;
};

//------------------------------------------------------------------------------
// Resources
//------------------------------------------------------------------------------
Texture2D	texTexture;

//------------------------------------------------------------------------------
// Samplers
//------------------------------------------------------------------------------
SamplerState 	samTexture;

//------------------------------------------------------------------------------
// Entry point
//------------------------------------------------------------------------------
float4 main( Fragment fragment ) : SV_TARGET
{
	return texTexture.Sample(samTexture, fragment.tex) * p_color;
}
