//------------------------------------------------------------------------------
// File: skysphere.fs
//
// Purpose: Fragment shader for skysphere
//------------------------------------------------------------------------------
#include "scene.fsh"

//------------------------------------------------------------------------------
// Structures
//------------------------------------------------------------------------------

// Fragment shader input data
struct Fragment
{
	float4 pos	: SV_POSITION;		// Fragment position
	float2 tex	: TEXCOORD0;		// Texture coordinates
};

//------------------------------------------------------------------------------
// Resources
//------------------------------------------------------------------------------
Texture2D 	texEnvironment;			// Skysphere texture
                                        
//------------------------------------------------------------------------------
// Samplers
//------------------------------------------------------------------------------
SamplerState	samEnvironment;			// Sampler state

//------------------------------------------------------------------------------
// Entry point
//------------------------------------------------------------------------------
float4 main( Fragment fragment ) : SV_TARGET
{
	return texEnvironment.Sample( samEnvironment, fragment.tex );
}
