//------------------------------------------------------------------------------
// File: grayscale.fs
//
// Purpose: "Grayscale" post-processing filter
//------------------------------------------------------------------------------
#include "filter.fsh"

//------------------------------------------------------------------------------
// Constants
//------------------------------------------------------------------------------
static float4 WEIGHTS = { 0.299f, 0.587f, 0.114f, 0.0f };

//------------------------------------------------------------------------------
// Entry point
//------------------------------------------------------------------------------
float4 main( Fragment fragment ) : SV_TARGET
{
	return dot( texImage.Sample(samImage, fragment.tex), WEIGHTS );
}
