//------------------------------------------------------------------------------
// File: color.fs
//
// Description: Modulates input texture with a specific color
//------------------------------------------------------------------------------
#include "filter.fsh"

//------------------------------------------------------------------------------
// Parameters
//------------------------------------------------------------------------------
cbuffer cb0 : register( b0 )
{
	float4		p_color;
};

//------------------------------------------------------------------------------
// Entry point
//------------------------------------------------------------------------------
float4 main( Fragment fragment ) : SV_TARGET
{
	return texImage.Sample( samImage, fragment.tex ) * p_color;
}
