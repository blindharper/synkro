//------------------------------------------------------------------------------
// File: sepia.fs
//
// Purpose: "Sepia" post-processing filter
//------------------------------------------------------------------------------
#include "filter.fsh"

//------------------------------------------------------------------------------
// Constants
//------------------------------------------------------------------------------
static float4 WEIGHTS = { 0.299f, 0.587f, 0.114f, 0.0f };

//------------------------------------------------------------------------------
// Entry point
//------------------------------------------------------------------------------
float4 main( Fragment fragment ) : SV_TARGET
{
	float4 gray = dot( texImage.Sample(samImage, fragment.tex), WEIGHTS );
	return gray * float4(1.2f, 1.0f, 0.8f, 1.0f);
}
